          function submitDetails(){
            $('#response').html("");
            var first_name,last_name,phone,email,address;
            first_name=last_name=phone=email=address = "";

            first_name = $("#f_name").val();
			      last_name = $("#l_name").val();
            phone = $("#phone").val();
            email = $("#email").val();
            address = $("#address").val();

            if(first_name != "" && phone != "" && email != "" && address !=""){
               var regexpEmail = /^([A-Za-z0-9_\-\.])+\@([A-Za-z0-9_\-\.])+\.([A-Za-z]{2,4})$/;
                if (!$("#email").val().match(regexpEmail)) {
                    $('#response').html("Please enter a valid Email!").css("color",'red');
                }
                else{
                  $("#submit").html('Please Wait...').addClass('subBtn');
                  $("#submit").attr("disabled", true);
                  // Ajax Request
                $.ajax({
                    method: "POST",
                    url: "php/php pdf/careerForm.php",
                    data : {
                        vfname : first_name,
                        vlname : last_name,
                        vphone : phone,
                        vemail : email,
                        vaddress : address
                    },
                    success : function(resp) {
                      var res = JSON.parse(resp);
                      console.log(res.data[0].error);
                       if (res.data[0].error == "false"){
                        $("#response").html("Congratulations! Your details has been submitted successfully.").css("color", "green");
                        setTimeout("$('#response').html(''); document.getElementById('joinForm').reset();$('#submit').html('Submit').removeClass('subBtn'); $('#submit').attr('disabled', false);", 3000);

                      }

                      else{
                        console.log(res.data[0].error);
                        $("#submit").html('Submit').removeClass('subBtn');
                        $("#submit").attr("disabled", false);
                        $('#response').html('There was an error trying to send your message. Please try again').css("color",'red');
                        setTimeout("$('#response').html('');", 3000);
                      }
                    }
                });
                }
            }

            else{
              $("#response").html("Please fill all the fields!").css("color", "red");
            }
          }
        </script>
    
