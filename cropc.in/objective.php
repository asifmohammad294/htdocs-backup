                        <h2 class="content-inner-title">Climate Resilient Observing Systems Promotion Council (CROPC)<span class="bold-text"></span></h2>
                              <p>The objective is to create a Climate Change risk resilient Council through promotion of
Resilient Observing systems thereby enable impact based community centric forecasting
through a regulatory mechanism to last mile in user friendly manner , thus reducing sufferings
to humanity, animals and minimize economic losses.</p>
<br>
<p>
The CROPC will function as Center for promotion of Climate Resilient Observing
Systems and public notification system and will be a platform for various professionals and
technical and will function in following fields :-
<li> Technical body to advise states in setting up Climate Resilient Observing Systems
and Emergency communication systems.
</li>
<li> Weather Data Collection and Dissemination Protocol
</li>
<li> Promotion of Weather Observing Systems especially Automatic Weather
Stations down to Community level, through Government and Non Government
Organizations in Agriculture, Industries, education etc.
</li>
<li> Create Innovative platforms of versatile Communication to serve community
with hazard warning and climate change adaptation through Government,
private and partner agencies .
</li>
<li> Weather and Climate Resilience Activities- Immediate, short and long term
activities. Local level platform to be created amongst community, with special
focus on Disaster Hotspots
</li>
<li> Policy Advocacy.
</li>
<li> Create wide spread multi spectrum Visibility platforms.
</li>
<li> Research and Development.
</li>
<li> Knowledge sharing platform
</li>
<li> Project Management and Consultancy services
</li>
<li> Create Hazard safe hutments in highly sensitive zone on priority.
</li>
<li> Facilitate lateral use of weather and climate change inputs in various traditional
and emerging fields.
</li>
<li> Any other emerging and functional requirements
</li>
